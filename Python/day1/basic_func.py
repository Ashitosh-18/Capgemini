import math

# Absolute Value (abs):

number = -7
result = abs(number)
print("Absolute value of -7:", result)

# Power (math.pow):

base = 2
exponent = 3
result = math.pow(base, exponent)
print("2 raised to the power 3:", result)

# Square Root (math.sqrt):

number = 16
result = math.sqrt(number)
print("Square root of 16:", result)